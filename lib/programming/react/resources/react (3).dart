import 'package:flutter/material.dart';
import 'package:webview_flutter/webview_flutter.dart';

class ReactResource3 extends StatefulWidget {
  const ReactResource3({super.key});

  @override
  State<ReactResource3> createState() => _ReactResource3State();
}

class _ReactResource3State extends State<ReactResource3> {
  late WebViewController _controller;
  @override
  void initState() {
    super.initState();
    _controller = WebViewController();
  }

  @override
  Widget build(BuildContext context) {
    return WillPopScope(
      onWillPop: () async {
        if (await _controller.canGoBack()) {
          _controller.goBack();
          return false;
        } else {
          return true;
        }
      },
      child: WebViewWidget(
        controller: _controller
          ..setJavaScriptMode(JavaScriptMode.unrestricted)
          ..loadRequest(
            Uri.parse(
                'https://jasonwatmore.com/post/2020/06/02/react-setup-development-environment'),
          )
          ..setNavigationDelegate(
            NavigationDelegate(
              onPageFinished: (String url) {
                _controller.runJavaScript(
                  """
            document.querySelector('header').style.display = 'none';
            document.querySelector('footer').style.display = 'none';
            """,
                );
              },
            ),
          ),
      ),
    );
  }
}
